package day04_Variables;

public class Currencies {
        // ten different currencies
    public static void main(String[] args) {

        int dollar = 1000;

        double lira = dollar * 18.23;
        double euro = dollar * 0.98;
        double sterlin = dollar * 0.86;
        double swissFranc = dollar * 0.96;
        double yuan = dollar * 6.92;
        double yen = dollar * 142;
        double riyal = dollar * 3.75;
        double rupee = dollar * 79.67;
        double dinar = dollar * 1_459;
        double bitcoin = dollar * 0.0000461559;


        System.out.println("1000 DOLLAR");
        System.out.println("bitcoin = " + bitcoin);
        System.out.println("dinar = " + dinar);
        System.out.println("rupee = " + rupee);
        System.out.println("riyal = " + riyal);
        System.out.println("yen = " + yen);
        System.out.println("yuan = " + yuan);
        System.out.println("swissFranc = " + swissFranc);
        System.out.println("sterlin = " + sterlin);
        System.out.println("euro = " + euro);
        System.out.println("lira = " + lira);






    }

}
