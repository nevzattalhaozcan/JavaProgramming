package day15_ForLoop;

import day02_HelloWorld.HelloWorld;

public class ForLoopIntro {

    public static void main(String[] args) {

        for(int i = 1; i <= 10; ++i){

            System.out.print(i + " ");

        }

        System.out.println();

        for(int i = 1; i <= 5; ++i){

            System.out.println("Hello World!");

        }

    }
}
